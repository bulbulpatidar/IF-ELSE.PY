a=int(input("enter the num:-"))
b=int(input("enter the num:-"))
sign=input("enter the sign:-")
if sign=="+":
    print(a+b)
elif sign=="-":
    print(a-b) 
elif sign=="*":
    print(a*b)
elif sign=="/":
    print(a/b)
elif sign=="//":
    print(a//b)
elif sign=="%":
    print(a%b)
else:
    print("nothing")                       